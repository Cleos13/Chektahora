  <p>Las ITS son un tema que muchas personas evitan discutir debido al estigma social sobre el diagnóstico y la falta de información precisa. Sin embargo, es importante abordarlo con mente abierta y de manera consciente, ya que solo informándonos podremos prevenir, diagnosticar pero sobre todo tratar de manera oportuna.</p>

  <div>
    <h2>¿Qué son las Infecciones de Transmisión Sexual?</h2>
    <p>En este blog te responderemos algunas preguntas básicas sobre las ITS como qué son, cómo se transmiten, cómo prevenirlas y tratarlas.</p>
    <p>Un grupo de enfermedades que se contagian durante las relaciones sexuales sin protección y algunas otras también se pueden contagiar aún utilizando métodos de protección de barrera como el condón. Son causadas por virus, bacterias, hongos y otros gérmenes microscópicos.</p>
    <p>Una persona infectada puede seguir propagando la enfermedad sin saber que la tiene, si no presenta síntomas y la infección pasa desapercibida. </p>
    <p>Estas enfermedades las pueden presentar personas de cualquier edad y sexo o preferencia sexual, pero los adolescentes y adultos jóvenes son los que están en mayor riesgo.</p>
  </div>

  <div>
    <h2>¿Cuáles son las ITS más conocidas?</h2>
    <p>Tricomoniasis, clamidiasis, candidiasis o moniliasis vaginal, gonorrea o blenorragia, sífilis, chancro blando, herpes, VPH, condilomatosis, hepatitis virales y VIH/SIDA.</p>
  </div>

  <div>
    <h2>¿Cuáles son las complicaciones de las ITS?</h2>
    <p>Si no se tratan a tiempo pueden afectar de sobremanera tu salud sexual, ocasionando esterilidad, enfermedades crónicas e incluso la muerte.</p>
  </div>

  <div>
    <h2>¿Cómo se transmiten?</h2>
    <ul>
      <li>Contacto sexual sin protección con personas infectadas.</li>
      <li>Transfusiones con sangre no segura o no certificada.</li>
      <li>Uso de agujas contaminadas de personas infectadas.</li>
      <li>Por medio de la placenta de la madre enferma, quien transmite al feto o al recién nacido la ITS.</li>
      <li>Por medio de heridas en el canal del parto (vagina) cuando tiene heridas durante el proceso de nacimiento.</li>
      <li>A través de la lactancia materna de una madre enferma de VIH/SIDA.</li>
    </ul>
  </div>

  <div>
    <h2>¿Si tengo sexo anal puedo contraer una ITS?</h2>
    <p>Sí, porque para contraer algunas ITS solo es necesario el roce de piel a piel o piel a mucosas, además, en el caso del ano, su tejido superficial es muy delgado y no lubrica, por lo que es muy propenso a lesionarse fácilmente y esto permite el paso del virus al organismo.</p>

    <h2>¿Las ITS se pueden transmitir si no hay penetración?</h2>
    <p>Sí, como te platicamos en el punto anterior, algunas infecciones se transmiten solo por contacto piel a piel, además, antes de una penetración puede haber ya presencia de líquido preseminal, que es potencialmente infectante, o de flujo vaginal, mismos que también aumentan el riesgo de contagio.</p>
  </div>

  <div>
    <h2>¿Cómo se puede reducir el riesgo de contagio?</h2>
    <p>La prevención es la mejor forma de evitar una infección de transmisión sexual. Muchas personas infectadas pueden no saberlo al no tener síntomas, por tanto, la recomendación es utilizar protección en cualquier relación sexual, siempre que no sepamos con certeza que la otra persona esté sana (lo que supone que se haya hecho recientemente las pruebas correspondientes y estas hayan salido negativas).</p>
    <p>El contagio de muchas ITS puede prevenirse utilizando un método de barrera durante las relaciones sexuales, esto incluye también las relaciones del sexo oral.</p>
   
    <ul>
      <li>Con el uso adecuado y frecuente de condón (en cada relación sexual).</li>
      <li>Siendo monógamo (que tu pareja y tu sólo tengan relaciones sexuales entre ustedes).</li>
      <li>No realizando prácticas sexuales adicionales de riesgo como: relaciones anales o vaginales sin utilizar preservativos o utilizando solo medicamentos para prevenir o tratar el VIH sin cuidarse, (estos métodos no son 100% seguros).</li>
    </ul>
    <p>Los comportamientos de riesgo como tener relaciones sexuales sin preservativo y tener múltimples parejas sexuales o el tener contacto sexual con personas conocidas con infección activa con alguna ITS, potencializan el riesgo.</p>
  </div>

