Estas bacterias carecen de pared celular, lo que les permite resistir el tratamiento con antibióticos comunes como la penicilina. Los Mollicutes que infectan el tracto genital son Mycoplasma genitalium, Mycoplasma hominis y Ureaplasma urealyticum. <br> 
Ureaplasma urealyticum es potencialmente patógeno y tiene un rol causal en infecciones del tracto urinario y genital. Se considera además la causa más reconocida de infertilidad en hombres, al reducir la calidad del semen y el potencial de fertilización del espermatozoide. <br>Mycoplasma hominis está asociada a la enfermedad inflamatoria pélvica y producir rotura prematura de membranas durante el embarazo. En la mujer, la infección es más frecuente que en el hombre y al igual que Clamidia generalmente es asintomática. La frecuencia de estos microorganismos se asocia a trastornos como el aborto involuntario, infertilidad, enfermedad pélvica inflamatoria, orquitis, epididimitis y prostatitis. <br>  Los Mollicutes se encuentran frecuentemente en el tracto genital y semen de hombres fértiles e infértiles. En hombres fértiles de parejas infértiles existe una alta frecuencia de U. urealyticum, M. genitalium y M. hominis, que se ha asociado a una disminución en el conteo espermático. <br>  Asimismo, hombres infectados por M. Hominis presentan una reducción de la motilidad y concentración en el conteo espermático además de cambios en su morfología, en cambio la infección por U. urealyticum e infecciones mixtas evidencian un aumento en la viscosidad y acidez del líquido seminal.  <br>  Treponema pallidum (Sífilis)
